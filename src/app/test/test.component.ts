  fileRead(e: any) {
    if (e.target.files) {
      var reader = new FileReader();
      reader.readAsDataURL(e.target.files[0]);
      reader.onload = (event: any) => {
        var obj = event.target.result;
        console.log(obj);
      };
    }
  }
